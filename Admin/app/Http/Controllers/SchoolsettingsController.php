     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $teams = Team::findOrFail($id);
        $teams->description = $request->description;
        $teams->contact = $request->contact_number;
        $teams->latitude = $request->school_latitude;
        $teams->longtitude = $request->school_longtitude;

        $teams->save();
        toast('School profile has been updated.','success');
        return redirect()->route('myschool.index');
    }

